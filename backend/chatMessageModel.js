import mongoose from "mongoose"

const chatMessageSchema = new mongoose.Schema({
  message: {
    type: String,
    required: true,
    minlength: 5,
    maxlength: 140,
  },
  hearts: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
})

const ChatMessage = mongoose.model("ChatMessage", chatMessageSchema)

export default ChatMessage
